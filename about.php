        <h3>Whole-Systems Engineering</h3>
        <p>
          Whole-Systems design practices are founded in organization theory and have traditionally been applied in medicine, automotive, and energy sectors.  This more comprehensive approach to
          development abandons orthodox methods of evolving systems in small, isolated, segments responsible for bottlenecks or usage issues.  Instead, Whole-Systems Engineering evaluates each design
          decision's impact on all components of the system, maintaining a sense of the big picture even in high complexity systems.  This more tedious but thorough approach leads to slow, steady improvements
          in core technology, but also yields efficient, durable systems with a greater range of application.
        </p>
      </div>
